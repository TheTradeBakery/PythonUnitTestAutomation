def mul(a,b):
    """
    >>> mul(2, 3)
    6
    >>> mul('a', 2)
    'aa'

    :param a:
    :param b:
    :return:
    """
    return a * b

def add(a,b):
    """
    >>> add(2,3)
    5
    >>> add('a','b')
    'ab'

    :param a:
    :param b:
    :return:
    """
    return a + b
